            <h2>Comment se déroule une demande de crédit chez le Crédit Lédonien ?</h2>
            <p>

                <strong>Prêt personnel :</strong> pour une demande de crédit à la consommation de plus de <strong>5000
                    euros</strong>. Sélectionnez ensuite le projet qui vous concerne, le montant, la durée et lancez le
                simulateur de crédit. Voici les différents types de projet que le <strong>Crédit Lédonien</strong> vous
                propose de financer via un prêt personnel : voiture et moto neuve ou d'occasion ; travaux, mobilier et
                décoration ; véhicule de loisirs pour un camping-car, une caravane ou un bateau et projet divers pour un
                événement de vie (un mariage, une naissance, une fête de famille, un divorce ou des funérailles) ou le
                financement des études supérieures de vos enfants, de voyages, de loisirs ou de votre santé.<br>

                <br> <strong>Crédit renouvelable :</strong> pour une somme d'argent jusqu’à <strong>3000 euros</strong>
                que
                vous pouvez utiliser sans justificatif dans l'ensemble du réseau Mastercard, en boutique comme sur
                Internet,. Sélectionnez ensuite le montant de votre choix entre <strong>500</strong> et <strong>3000
                    euros</strong> et la durée de remboursement souhaitée, puis cliquez sur simuler.<br>

                <br> <strong>Rachat de crédit :</strong> si vous souhaitez regrouper un ou plusieurs crédits. Ne prenez
                jamais un crédit à la consommation pour rembourser un crédit existant ! En tant qu'organisme de crédit
                responsable, le <strong>Crédit Lédonien</strong> vous accompagne et vous propose une offre de rachat et
                de
                regroupement de crédits adaptée à votre situation afin de vous permettre d'honorer vos échéances dans le
                respect de votre budget.
            </p>
